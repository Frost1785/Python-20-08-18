import turtle
import time

eric = turtle.Turtle()
canvas = turtle.Screen()
eric.shape('turtle')
eric.speed(50)
eric.color('dark green')
canvas.title('Canvas')
canvas.bgcolor('light cyan')
canvas.colormode(255)
eric.pencolor('red')
eric.pensize(25)
eric.forward(100)
time.sleep(2)
eric.left(90)
eric.forward(100)
time.sleep(1.5)
eric.left(90)
eric.forward(100)
time.sleep(1.5)
eric.left(90)
eric.forward(100)
time.sleep(2)
eric.right(90)
eric.penup
eric.forward(100)
eric.pendown
eric.write("Hello")


turtle.done